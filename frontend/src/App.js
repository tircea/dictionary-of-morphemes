function App() {
  const [searchMode, setSearchMode] = useState('word');
  const [wordDetails, setWordDetails] = useState(null);
  const [suggestions, setSuggestions] = useState([]);
  const [drawerOpen, setDrawerOpen] = useState(false);
  const [currentView, setCurrentView] = useState('search');
  const [currentSearchTerm, setCurrentSearchTerm] = useState('');
  const [showNotFoundMessage, setShowNotFoundMessage] = useState(false);
  const componentSearchRef = useRef(null);

  const handleSearch = async (searchTerm) => {
    try {
      setCurrentSearchTerm(searchTerm);
      const result = await searchWord(searchTerm);
      if (result.found) {
        setWordDetails(result.word);
        setSuggestions([]);
        setShowNotFoundMessage(false);
      } else {
        setWordDetails(null);
        setSuggestions(result.suggestions);
        setShowNotFoundMessage(searchTerm.length > 4);
      }
      return result;
    } catch (error) {
      console.error('Error searching:', error);
    }
  };

  const handleWordSelect = (word) => {
    setWordDetails(word);
    setSuggestions([]);
    setSearchMode('word');
  };

  const handleComponentSelect = (type, id) => {
    setSearchMode('component');
    // We need to wait for the component search tab to be active before selecting the component
    setTimeout(() => {
      if (componentSearchRef.current && typeof componentSearchRef.current.selectComponent === 'function') {
        componentSearchRef.current.selectComponent(type, id);
      }
    }, 100);
  };

  const handleDrawerToggle = () => {
    setDrawerOpen(!drawerOpen);
  };

  const menuItems = [
    { text: 'Пошук', icon: <SearchIcon />, view: 'search' },
    { text: 'Про словник', icon: <InfoIcon />, view: 'info' }
  ];

  return (
    <ThemeProvider theme={theme}>
      <CssBaseline />
      <Box sx={{ display: 'flex' }}>
        <AppBar position="fixed">
          <Toolbar>
            <IconButton
              color="inherit"
              edge="start"
              onClick={handleDrawerToggle}
              sx={{ mr: 2 }}
            >
              <MenuIcon />
            </IconButton>
            <Typography variant="h6" noWrap component="div" sx={{ flexGrow: 1 }}>
              Морфологічний аналізатор
            </Typography>
          </Toolbar>
        </AppBar>

        <Drawer
          variant="temporary"
          anchor="left"
          open={drawerOpen}
          onClose={handleDrawerToggle}
          sx={{
            width: 240,
            flexShrink: 0,
            '& .MuiDrawer-paper': {
              width: 240,
              boxSizing: 'border-box',
            },
          }}
        >
          <Toolbar />
          <Divider />
          <List>
            {menuItems.map((item) => (
              <ListItem 
                button 
                key={item.text}
                onClick={() => {
                  setCurrentView(item.view);
                  setDrawerOpen(false);
                }}
              >
                <ListItemIcon>{item.icon}</ListItemIcon>
                <ListItemText primary={item.text} />
              </ListItem>
            ))}
          </List>
        </Drawer>

        <Box
          component="main"
          sx={{
            flexGrow: 1,
            p: 3,
            width: '100%',
            minHeight: '100vh',
            backgroundColor: 'background.default',
            mt: 8
          }}
        >
          {currentView === 'search' ? (
            <Container maxWidth="lg">
              <Box sx={{ width: '100%', maxWidth: 600, margin: '0 auto', mb: 2 }}>
                <ToggleButtonGroup
                  value={searchMode}
                  exclusive
                  onChange={(e, newMode) => newMode && setSearchMode(newMode)}
                  aria-label="search mode"
                  fullWidth
                  sx={{
                    '& .MuiToggleButton-root': {
                      py: 1.5,
                      fontSize: '1rem',
                      fontWeight: 500,
                      '&.Mui-selected': {
                        backgroundColor: 'primary.main',
                        color: 'white',
                        '&:hover': {
                          backgroundColor: 'primary.dark',
                        }
                      }
                    }
                  }}
                >
                  <ToggleButton value="word" aria-label="word search">
                    Пошук за словом
                  </ToggleButton>
                  <ToggleButton value="component" aria-label="component search">
                    Пошук за компонентами
                  </ToggleButton>
                </ToggleButtonGroup>
              </Box>
              
              {searchMode === 'word' ? (
                <>
                  <SearchBar 
                    onSearch={handleSearch}
                    searchMode={searchMode}
                    hideToggleButtons={true}
                  />
                  {showNotFoundMessage && (
                    <Paper sx={{ mt: 2, p: 2 }}>
                      <Alert severity="info">
                        На жаль, слово "{currentSearchTerm}" не знайдено в словнику.
                      </Alert>
                    </Paper>
                  )}
                  {suggestions && suggestions.length > 0 && (
                    <Suggestions 
                      suggestions={suggestions} 
                      onSuggestionClick={handleSearch}
                    />
                  )}
                  {wordDetails ? (
                    <WordDetails word={wordDetails} />
                  ) : (
                    <>
                      {/* Add TopComponents here */}
                      <TopComponents onComponentSelect={handleComponentSelect} />
                      <AlphabetWordList onWordSelect={handleWordSelect} />
                    </>
                  )}
                </>
              ) : (
                              <ComponentSearch 
                onWordSelect={handleWordSelect} 
                ref={componentSearchRef}
              />
              )}
            </Container>
          ) : (
            <Container maxWidth="lg">
              <DictionaryInfo />
            </Container>
          )}
        </Box>
      </Box>
    </ThemeProvider>
  );
}

export default App; 